import UIKit



extension UIColor {

    func getRGB(r: CGFloat,g: CGFloat,b: CGFloat,alpha: CGFloat) -> UIColor {
    
        
         return UIColor(red: r/255, green: g/255, blue: b/255, alpha: alpha)
        
    }
    
    
    
}
